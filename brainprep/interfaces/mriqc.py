import os
from pathlib import Path
from typing import Union

from ..reporting import log_runtime
from ..typing import (
    Directory,
    File,
)
from ..utils import (
    coerceparams,
    outputdir,
)
from ..wrappers import cmdwrapper


@log_runtime(
    bunched=False)
@cmdwrapper
@outputdir
@coerceparams
def subject_level_qa(
        image_files: list[File],
        output_dir: Directory) -> tuple[list[str], tuple[list[File]]]:
    """
    Compute subject level Image Quality Metrics (IQMs) generated by MRIQC.

    Parameters
    ----------
    image_files : list[File]
        Path to the input image files.
    output_dir : Directory
        Directory where the IQMs will be saved.

    Returns
    -------
    command : list[str]
        Cross sectional quality analysis command-line.
    workspace_dir : Directory
        The workspace of the current processing.
    iqm_files : list[File]
        IQM files.

    Raises
    ------
    ValueError
        If the output directory does not exist.
    """
    rawdata_dir = image_files[0].parent.parent.parent.parent
    subject = output_dir.parent.name.replace("sub-", "")
    workspace_dir = output_dir / "workspace"
    output_dir = output_dir.parent.parent

    iqm_files = [
        path.parent / f"{path.name.split('.')[0]}.json"
        for path in image_files
    ]
    iqm_files = [
        Path(
            str(path).replace(
                str(rawdata_dir),
                str(output_dir)
            )
        )
        for path in iqm_files
    ]

    command = [
        "mriqc",
        str(rawdata_dir),
        str(output_dir),
        "participant",
        "-w", str(workspace_dir),
        "--no-sub",
        "--no-datalad-get",
        "--notrack",
        "--participant-label", subject,
    ]

    return command, (workspace_dir, iqm_files)


@log_runtime(
    bunched=False)
@cmdwrapper
@outputdir
@coerceparams
def group_level_qa(
        modalities: list[str],
        output_dir: Directory) -> tuple[list[str], tuple[File]]:
    """
    Compute group level Image Quality Metrics (IQMs) generated by MRIQC.

    Parameters
    ----------
    modalities: list[str]
        The modalities in the current study.
    output_dir : Directory
        Directory where the IQMs will be saved.

    Returns
    -------
    command : list[str]
        Group quality analysis command-line.
    iqm_files : list[File]
        Group IQM files.

    Raises
    ------
    ValueError
        If the output directory does not exist.
    """
    rawdata_dir = output_dir.parent.parent / "rawdata"

    iqm_files = [
        output_dir / f"group_{mod}.tsv"
        for mod in modalities
    ]

    command = [
        "mriqc",
        str(rawdata_dir),
        str(output_dir),
        "group",
        "--no-sub",
        "--no-datalad-get",
        "--notrack",
    ]

    return command, (iqm_files, )
